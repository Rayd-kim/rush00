int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb < 0)
		return (0);
	while (i <= 46340 && i * i < nb)
		i++;
	if (i > 46340)
		return (0);
	if (i * i == nb)
		return (i);
	else
		return (0);
}
